CREATE TABLE MENUS
(
     MENU_ID     VARCHAR2(20)   NOT  NULL  PRIMARY KEY
   , MENU_NAME   VARCHAR2(120) NOT  NULL
)


CREATE TABLE USERS
(
    USERID     VARCHAR2(20)  PRIMARY KEY,
    PASSWD     VARCHAR2(20)  NOT NULL,
    NICKNAME   VARCHAR2(60)  NOT NULL,
    EMAIL      VARCHAR2(100) NOT NULL,
    USER_LOCAL VARCHAR2(100) NOT NULL,
    RIDER      VARCHAR2(10)  NOT NULL,
    INDATE     DATE          DEFAULT SYSDATE
)

CREATE TABLE BOARD
( BOARD_NUMBER NUMBER(5,0) PRIMARY KEY,
  MENU_ID VARCHAR2(20) NOT NULL REFERENCES MENUS(MENU_ID),
  TITLE   VARCHAR2(100) NOT NULL,
  WRITER VARCHAR2(100) NOT NULL REFERENCES USERS(NICKNAME),
  BOARD_LOCAL VARCHAR2(100),
  CONT VARCHAR2(4000),
  INDATE DATE DEFAULT SYSDATE
 )

CREATE TABLE REPLY
( REPLY_NUMBER NUMBER(10, 0) PRIMARY KEY,
  BOARD_NUMBER NUMBER(5,0) NOT NULL REFERENCES BOARD(BOARD_NUMBER),
  USERID VARCHAR2(20) NOT NULL REFERENCES USERS(USERID),
  CONT VARCHAR2(1000) NOT NULL,
  WRITER VARCHAR2(60) NOT NULL,
  INDATE DATE DEFAULT SYSDATE
 )
